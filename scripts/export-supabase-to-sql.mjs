function parseEnvFile(filePath) {
  const raw = fs.readFileSync(filePath, "utf8");
  const vars = {};
  for (const line of raw.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx < 0) continue;
    const key = trimmed.slice(0, idx).trim();
    let value = trimmed.slice(idx + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

function sqlLiteral(value) {
  if (value === null || value === undefined) return "null";
  if (typeof value === "boolean") return value ? "true" : "false";
  if (typeof value === "number")
    return Number.isFinite(value) ? String(value) : "null";
  const text = String(value).replace(/'/g, "''");
  return `'${text}'`;
}

function buildUpsert(tableName, columns, row) {
  const colList = columns.join(", ");
  const values = columns.map((c) => sqlLiteral(row[c])).join(", ");
  const updates = columns
    .filter((c) => c !== "id")
    .map((c) => `${c} = excluded.${c}`)
    .join(", ");

  return `insert into public.${tableName} (${colList}) values (${values}) on conflict (id) do update set ${updates};`;
}

async function exportTable(supabase, table) {
  const selectCols = table.columns.join(",");
  const { data, error } = await supabase
    .from(table.name)
    .select(selectCols)
    .order("created_at", { ascending: true });

  if (error) {
    throw new Error(`Failed reading ${table.name}: ${error.message}`);
  }

  return (data ?? []).map((row) => buildUpsert(table.name, table.columns, row));
}

function writeChunkFile({ filePath, tableName, part, sourceUrl, statements }) {
  const lines = [];
  lines.push("-- Generated by scripts/export-supabase-to-sql.mjs");
  lines.push("-- Chunked data transfer file for Supabase SQL Editor.");
  lines.push(`-- Source: ${sourceUrl}`);
  lines.push(`-- Table: ${tableName}`);
  lines.push(`-- Part: ${part}`);
  lines.push(`-- Generated at: ${new Date().toISOString()}`);
  lines.push("begin;");
  lines.push(`-- ${tableName}`);
  lines.push(...statements);
  lines.push("commit;");

  fs.writeFileSync(filePath, `${lines.join("\n")}\n`, "utf8");
}

function writeTableChunks({ tableName, sourceUrl, statements, startIndex }) {
  if (statements.length === 0) return { nextIndex: startIndex, files: [] };

  const files = [];
  let chunk = [];
  let chunkChars = 0;
  let part = 1;
  let fileIndex = startIndex;

  const flushChunk = () => {
    if (chunk.length === 0) return;

    const base = `${String(fileIndex).padStart(2, "0")}-${tableName}-part-${String(part).padStart(2, "0")}.sql`;
    const filePath = path.join(CHUNKS_DIR, base);
    writeChunkFile({
      filePath,
      tableName,
      part,
      sourceUrl,
      statements: chunk,
    });
    files.push(base);

    fileIndex += 1;
    part += 1;
    chunk = [];
    chunkChars = 0;
  };

  for (const stmt of statements) {
    const stmtChars = stmt.length + 1;
    const exceedsCount = chunk.length >= MAX_STATEMENTS_PER_CHUNK;
    const exceedsChars = chunkChars + stmtChars > MAX_CHUNK_CHARS;

    if (chunk.length > 0 && (exceedsCount || exceedsChars)) {
      flushChunk();
    }

    chunk.push(stmt);
    chunkChars += stmtChars;
  }

  flushChunk();
  return { nextIndex: fileIndex, files };
}

async function main() {
  if (!fs.existsSync(ENV_PATH)) {
    throw new Error(`Missing env file: ${ENV_PATH}`);
  }

  const env = parseEnvFile(ENV_PATH);
  const url = env.NEXT_PUBLIC_SUPABASE_URL;
  const serviceKey = env.SUPABASE_SERVICE_ROLE_KEY;

  if (!url || !serviceKey) {
    throw new Error(
      "Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in cms-app/.env.local",
    );
  }

  const supabase = createClient(url, serviceKey);
  const lines = [];
  const tableStatements = [];

  lines.push("-- Generated by scripts/export-supabase-to-sql.mjs");
  lines.push(
    "-- Run this file in the NEW Supabase project SQL Editor after schema migration.",
  );
  lines.push(`-- Source: ${url}`);
  lines.push(`-- Generated at: ${new Date().toISOString()}`);
  lines.push("begin;");

  for (const table of TABLES) {
    lines.push(`-- ${table.name}`);
    const stmts = await exportTable(supabase, table);
    tableStatements.push({ tableName: table.name, statements: stmts });
    if (stmts.length === 0) {
      lines.push(`-- no rows in ${table.name}`);
    } else {
      lines.push(...stmts);
    }
    lines.push("");
  }

  lines.push("commit;");

  fs.writeFileSync(OUT_PATH, `${lines.join("\n")}\n`, "utf8");
  console.log(`Wrote ${OUT_PATH}`);

  fs.rmSync(CHUNKS_DIR, { recursive: true, force: true });
  fs.mkdirSync(CHUNKS_DIR, { recursive: true });

  let fileIndex = 1;
  const manifest = [];
  manifest.push("# Run these files in order in NEW Supabase SQL Editor");
  manifest.push(`# Source: ${url}`);
  manifest.push(`# Generated at: ${new Date().toISOString()}`);
  manifest.push("");

  for (const t of tableStatements) {
    const result = writeTableChunks({
      tableName: t.tableName,
      sourceUrl: url,
      statements: t.statements,
      startIndex: fileIndex,
    });
    fileIndex = result.nextIndex;

    if (result.files.length === 0) {
      manifest.push(`# ${t.tableName}: no rows`);
      continue;
    }

    manifest.push(`# ${t.tableName}`);
    for (const name of result.files) {
      manifest.push(name);
    }
    manifest.push("");
  }

  const manifestPath = path.join(CHUNKS_DIR, "README-RUN-ORDER.txt");
  fs.writeFileSync(manifestPath, `${manifest.join("\n")}\n`, "utf8");
  console.log(`Wrote ${manifestPath}`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
