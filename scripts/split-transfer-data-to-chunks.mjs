/**
 * Split supabase/transfer-data.sql into transfer-chunks/ (one-time / regen).
 * Run: node scripts/split-transfer-data-to-chunks.mjs
 */
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(process.cwd());
const INPUT = path.join(ROOT, "supabase", "transfer-data.sql");
const CHUNKS_DIR = path.join(ROOT, "supabase", "transfer-chunks");
const MAX_STATEMENTS_PER_CHUNK = 200;
const MAX_CHUNK_CHARS = 300_000;

const TABLE_ORDER = [
  "photos",
  "tributes",
  "books",
  "articles",
  "interviews",
  "bulletins",
];

function parseStatements(sql) {
  const byTable = Object.fromEntries(TABLE_ORDER.map((t) => [t, []]));
  const body = sql
    .replace(/^[\s\S]*?\nbegin;\n/m, "")
    .replace(/\ncommit;[\s\S]*$/m, "");

  const re =
    /insert into public\.\w+[\s\S]*?on conflict \(id\) do update[\s\S]*?;/g;
  const statements = body.match(re) ?? [];

  for (const stmt of statements) {
    const m = stmt.match(/insert into public\.(\w+)/);
    const table = m?.[1];
    if (table && byTable[table]) {
      byTable[table].push(stmt.trim());
    }
  }

  return byTable;
}

function writeChunkFile({ filePath, tableName, part, statements }) {
  const lines = [
    "-- Generated by scripts/split-transfer-data-to-chunks.mjs",
    "-- Chunked data transfer file for Supabase SQL Editor.",
    `-- Table: ${tableName}`,
    `-- Part: ${part}`,
    `begin;`,
    `-- ${tableName}`,
    ...statements,
    "commit;",
    "",
  ];
  fs.writeFileSync(filePath, lines.join("\n"), "utf8");
}

function writeTableChunks({ tableName, statements, startIndex }) {
  if (statements.length === 0) return { nextIndex: startIndex, files: [] };

  const files = [];
  let chunk = [];
  let chunkChars = 0;
  let part = 1;
  let fileIndex = startIndex;

  const flushChunk = () => {
    if (chunk.length === 0) return;
    const base = `${String(fileIndex).padStart(2, "0")}-${tableName}-part-${String(part).padStart(2, "0")}.sql`;
    const filePath = path.join(CHUNKS_DIR, base);
    writeChunkFile({ filePath, tableName, part, statements: chunk });
    files.push(base);
    fileIndex += 1;
    part += 1;
    chunk = [];
    chunkChars = 0;
  };

  for (const stmt of statements) {
    const stmtChars = stmt.length + 1;
    if (
      chunk.length > 0 &&
      (chunk.length >= MAX_STATEMENTS_PER_CHUNK ||
        chunkChars + stmtChars > MAX_CHUNK_CHARS)
    ) {
      flushChunk();
    }
    chunk.push(stmt);
    chunkChars += stmtChars;
  }

  flushChunk();
  return { nextIndex: fileIndex, files };
}

function main() {
  if (!fs.existsSync(INPUT)) {
    console.error(`Missing ${INPUT}`);
    process.exit(1);
  }

  const sql = fs.readFileSync(INPUT, "utf8");
  const byTable = parseStatements(sql);

  fs.rmSync(CHUNKS_DIR, { recursive: true, force: true });
  fs.mkdirSync(CHUNKS_DIR, { recursive: true });

  let fileIndex = 1;
  const manifest = [
    "# Run these files in order in the Supabase SQL Editor",
    "# Run migrations/0001_complete_schema.sql first",
    "",
  ];

  for (const tableName of TABLE_ORDER) {
    const statements = byTable[tableName] ?? [];
    const result = writeTableChunks({
      tableName,
      statements,
      startIndex: fileIndex,
    });
    fileIndex = result.nextIndex;

    if (result.files.length === 0) {
      manifest.push(`# ${tableName}: no rows`);
      continue;
    }

    manifest.push(`# ${tableName}`);
    manifest.push(...result.files);
    manifest.push("");
  }

  fs.writeFileSync(
    path.join(CHUNKS_DIR, "README-RUN-ORDER.txt"),
    `${manifest.join("\n")}\n`,
    "utf8",
  );

  console.log(`Wrote ${CHUNKS_DIR}`);
}

main();
